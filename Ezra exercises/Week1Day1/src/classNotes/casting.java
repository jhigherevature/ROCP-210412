package classNotes;

public class casting {

	public static void main(String[] args) {
		int num = 65;
		double decimal = num;
		double decimal2 = 65.0;
		int num2 = (int)decimal2;
//		casting from double to int
		char letter = (char)num;
		System.out.println(letter);
		byte b = (byte)0110;
		System.out.println(b);
	}

}
