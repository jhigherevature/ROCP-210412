
public class Datatypes {

	public static void main(String[] args) {

		byte b = 120;
		short s = 12345;
		int i = 1_000_000;
		long l = 1_000_000_000l;
		float f = 1_000_000_000.00f;
		double d = 1_000_000_000.00;
		boolean bool = true;
		char c = 'N'; 
		
		System.out.println("Primitive Datatypes include:");
		System.out.println("Byte: " + b + ", " + "Short: " + s + ", " + "Int: " + i + ", " +
		"Long: " + l + ", " + "Float: " + f + ", " + "Double: " + d + ", " + "Boolean: " + bool + ", "
		+ "Char: " + c);
	}
}