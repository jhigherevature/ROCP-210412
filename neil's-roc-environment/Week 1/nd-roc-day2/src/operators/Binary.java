package operators;

public class Binary {
	public static void main(String[]args) {
		int x = 5;
		int y = 2;
		int z = 5;
		
		System.out.println(x * y);
		System.out.println(x / y);
		System.out.println(x % y);
		System.out.println(x + y);
		System.out.println(x - y);
		
		System.out.println(x > z);
		System.out.println(x > y);
		System.out.println(x < z);
		System.out.println(x < y);
		System.out.println(x >= z);
		System.out.println(x <= z);
	}
}

