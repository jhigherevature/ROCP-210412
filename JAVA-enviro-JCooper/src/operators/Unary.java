package operators;

public class Unary {
	public static void main(String[] args) {
		int post = 10;
		int pre = 10;
		
		System.out.println(post);
		System.out.println(post++);
		System.out.println(post + "\n");
		
		System.out.println(pre);
		System.out.println(++pre);
		System.out.println(pre);
		System.out.println("\n");

		System.out.println(true);
		System.out.println(false);
		System.out.println("\n");

		System.out.println(!true);
		System.out.println(!false);
		System.out.println("\n");

	}
}
