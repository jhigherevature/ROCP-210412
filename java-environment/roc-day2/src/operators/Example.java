package operators;

public class Example {
	/*
	 * use CTRL + Space for the context menu in STS
	 */
	public static void main(String[] args) {
		// '\n' is used for new line characters
		System.out.print("This is print");
		
		// println prints message and adds new line character to the end
		System.out.println("This is println");
		// print only prints the message
		System.out.print("This is print\n");
		System.out.print("This is print");
	}
}
