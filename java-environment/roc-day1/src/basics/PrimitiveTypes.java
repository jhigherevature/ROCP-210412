package basics;

public class PrimitiveTypes {
	/*
	 * A variable is a construct that is used to hold onto some information.
	 * 
	 * In Java, we can specify two types of variables. Primitives and References.
	 * 
	 * Here are the primitives.
	 */
	byte b = 127; // whole number 8 bit
	short s = 12345; // whole number 16 bit
	int i  = 1_000_000; // whole number 32 bit
	long l = 1_000_000_000l; // whole number 64 bit
	float f = 1_000_000_000.00f; // decimal value 32 bit
	double d = 1_000_000_000.00d; // decimal value 64 bit;
	boolean bool = true; // either true or false
	char c = 'c'; // alpha characters using singular quotes - can be referenced as numbers as well

}
