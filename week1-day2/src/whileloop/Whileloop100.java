package whileloop;

public class Whileloop100 {
/*
 *   using a while loop, print even numbers from 100 to -100 on a separate line
 */
	public static void main(String[] args) {
		
		 int num=100;
	     System.out.println("Even numbers from 100 to -100");
	     while(num>-101)
	        {
	            System.out.println(num);
	            num-=2;
	        }
	}

}
