package whileloop;

public class WhileLoop1thru10 {
/*
 *   using a while loop, print the numbers from 1 to 10 on a separate line
 */
	public static void main(String[] args) {
		 int num=1;
	     System.out.println("Print numbers from 1 to 10 using While loop");
	     while(num<11)
	        {
	            System.out.println(num);
	            num+=1;
	        }

	}

}
