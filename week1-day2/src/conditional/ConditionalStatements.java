package conditional;

public class ConditionalStatements {
/*
 *   print boolean value to the console
 */
	public static void main(String[] args) {
		boolean user = true;
		if (user == true) {
		   System.out.println("Value is true");
		}	
		else {
		   System.out.println("Value is false");
	    }
	}

}
