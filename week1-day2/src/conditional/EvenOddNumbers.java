package conditional;

public class EvenOddNumbers {
/*
 *   print even and odd values to the concole
 */
	public static void main(String[] args) {
		for (int i = 1; i <=10; i++) {
		   if (i % 2 == 0)
			   System.out.println(i + " Even value");
		   else
			   System.out.println(i + " False value");
		}
				

	}

}
