package basics;

public class Datatypes {

	public static void main(String[] args) {
		// byte short char int long float double boolean 
		
		byte num = 127;
		System.out.println("data type byte num = " + num);
		
		short s = 32767; 
		System.out.println("data type short s = " + s);
		
		char ch = 'Z';
		System.out.println("data type char ch = " + ch);
		
		int i = 872112;
		System.out.println("data type int i = " + i);
		
		long l = 314422445L;
		System.out.println("data type long l = " + l);
		
		float f = 2323.2f;
		System.out.println("data type float f = " + f);
		
		double d = 232323.3434;
		System.out.println("data type double = " + d);
		
		boolean b1 = true;
		System.out.println(b1);
	}

}
